export function getModelLogo(modelId?: string, params?: { isLight?: boolean }) {
  const { isLight = true } = params ?? {};

  if (!modelId) {
    return undefined;
  }

  const lowerrModelId = modelId.toLowerCase();

  for (const info of LOGO_SET) {
    if (info.regex.test(lowerrModelId)) {
      return info[isLight ? 'light' : 'dark'];
    }
  }

  return undefined;
}
